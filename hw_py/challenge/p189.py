string=input("정수 입력해라")
a=int(string)

if a%2==0:
    print(a,"는 2로 나누어 떨어진다")
else:
    print(a,"는 2로 나누어 떨어지는 숫자가 아니다")

if a%3==0:
    print(a,"는 3으로 나누어 떨어진다")
else:
    print(a,"는 3으로 나누어 떨어지는 숫자가 아니다")

if a%4==0:
    print(a,"는 4로 나누어 떨어진다")
else:
    print(a,"는 4로 나누어 떨어지는 숫자가 아니다")